// explicit-ownership techstack probe (kept probe artifact; see ## Tech Stack).
// Run from anywhere: node docs/specs/explicit-ownership/probes/probe-id-anchor.mjs
// Falsifies-or-confirms: "id-anchored block splitting preserves the bulleted format's behaviour."
// Does NOT touch the product tree. Re-implements the candidate splitter + the three readers that
// consume it, and diffs old-vs-new over the five in-tree spec chains.
import { parseSpec } from '../../../../checker/sdlc-check.mjs';
import fs from 'node:fs';

const SPECS = ['enforcement-spine', 'repo-setup', 'visual-aids', 'adoption-quickwins', 'spec-location-under-docs'];

// --- current behaviour (mirrors sdlc-check.mjs as shipped) -------------------
const TOP_BULLET_RE = /^-\s+\*\*/;
const ID_DEFINITION_RE = /\*\*(AC|C|T)-(\d+)\b/g;
const TRACE_FIELD_RE = /\*(Advances|Component|Deps):\*\s*([^.]*)\./g;
const ID_REF_RE = /\b(AC|C|T)-\d+(?:\/\d+)*\b/g;

function splitOld(bodyLines) {
  const blocks = []; let cur = null;
  bodyLines.forEach((line, i) => {
    if (TOP_BULLET_RE.test(line)) { if (cur) blocks.push(cur); cur = { startIdx: i, lines: [line] }; }
    else if (cur) cur.lines.push(line);
  });
  if (cur) blocks.push(cur);
  return blocks.map((b) => { ID_DEFINITION_RE.lastIndex = 0; const m = ID_DEFINITION_RE.exec(b.lines.join('\n'));
    return { ...b, owner: m ? `${m[1]}-${m[2]}` : null }; });
}

// --- candidate behaviour: id-anchored ---------------------------------------
// definition site = first non-whitespace content, after an optional list marker, is a bold-lead id.
const DEFINITION_SITE_RE = /^\s*(?:[-*+]\s+)?\*\*(AC|C|T)-(\d+)\b/;

function splitNew(bodyLines) {
  const blocks = []; let cur = null;
  bodyLines.forEach((line, i) => {
    const m = DEFINITION_SITE_RE.exec(line);
    if (m) { if (cur) blocks.push(cur); cur = { startIdx: i, lines: [line], owner: `${m[1]}-${m[2]}` }; }
    else if (cur) cur.lines.push(line);
  });
  if (cur) blocks.push(cur);
  return blocks;
}

// Refinement under test: a block ALSO closes at a subheading. Without this the last id in a
// subsection swallows every trailing line until the next definition site (AC-18 absorbed the
// negative criteria + verification map and got its answer from downstream text).
const SUBHEADING_RE = /^###\s+(.+?)\s*$/;
function splitNew2(bodyLines) {
  const blocks = []; let cur = null;
  bodyLines.forEach((line, i) => {
    const m = DEFINITION_SITE_RE.exec(line);
    if (m) { if (cur) blocks.push(cur); cur = { startIdx: i, lines: [line], owner: `${m[1]}-${m[2]}` }; }
    else if (SUBHEADING_RE.test(line)) { if (cur) blocks.push(cur); cur = null; }
    else if (cur) cur.lines.push(line);
  });
  if (cur) blocks.push(cur);
  return blocks;
}

// --- readers (identical logic, parameterised by splitter) --------------------
function classify(sections, split) {
  const types = new Map();
  for (const section of sections) for (const b of split(section.body.split('\n'))) {
    if (!b.owner || !b.owner.startsWith('AC-')) continue;
    if (types.has(b.owner)) continue;
    const t = b.lines.join('\n');
    const decl = /verification type:\s*\*{0,2}\s*(reviewer[-\s]?checked|test[-\s]?backed)/i.exec(t);
    let type = null;
    if (decl) type = /reviewer/i.test(decl[1]) ? 'reviewer-checked' : 'test-backed';
    else if (/reviewer[-\s]?checked/i.test(t)) type = 'reviewer-checked';
    else if (/test[-\s]?backed/i.test(t)) type = 'test-backed';
    types.set(b.owner, type);
  }
  return types;
}

function expand(raw) {
  const out = new Set(); ID_REF_RE.lastIndex = 0; let m;
  while ((m = ID_REF_RE.exec(raw))) { const [head, ...tail] = m[0].split('/'); out.add(head);
    for (const t of tail) out.add(`${m[1]}-${t}`); }
  return out;
}

function fieldTraces(sections, split) {
  const traces = [];
  for (const section of sections) for (const b of split(section.body.split('\n'))) {
    if (!b.owner) continue;
    const t = b.lines.join('\n'); TRACE_FIELD_RE.lastIndex = 0; let m;
    while ((m = TRACE_FIELD_RE.exec(t))) traces.push({ from: b.owner, kind: m[1].toLowerCase(), refs: [...expand(m[2].trim())] });
  }
  return traces;
}

// --- run ---------------------------------------------------------------------
const lines = [];
const say = (s) => { lines.push(s); console.log(s); };
say('# Probe: id-anchored block splitting vs shipped bullet-only splitting');
say(`# node ${process.version} | ${SPECS.length} in-tree spec chains | generated by an agent-sdlc techstack spike`);
say('');

let verdict = true;
for (const name of SPECS) {
  const p = new URL(`../../${name}/${name}.md`, import.meta.url);
  if (!fs.existsSync(p)) continue;
  const model = parseSpec(fs.readFileSync(p, 'utf8'), p);
  const secs = model.sections;
  const declared = (fs.readFileSync(p, 'utf8').match(/verification type:/gi) || []).length;
  const oldT = classify(secs, splitOld);
  const newT = classify(secs, splitNew);       // A: definition site only
  const new2T = classify(secs, splitNew2);     // B: definition site + subheading boundary
  const acs = model.ids.filter((i) => i.kind === 'AC').length;

  const oldF = fieldTraces(secs, splitOld).filter((t) => t.from.startsWith('T-') && t.kind === 'advances');
  const new2F = fieldTraces(secs, splitNew2).filter((t) => t.from.startsWith('T-') && t.kind === 'advances');
  const oldOwners = new Set(oldF.map((t) => t.from)), new2Owners = new Set(new2F.map((t) => t.from));

  say(`## ${name}`);
  say(`   criteria defined                 : ${acs}   (verification-type declarations in source: ${declared})`);
  say(`   classified OLD / A / B           : ${oldT.size} / ${newT.size} / ${new2T.size}`);
  say(`   typed (non-null) OLD / A / B     : ${[...oldT.values()].filter(Boolean).length} / ${[...newT.values()].filter(Boolean).length} / ${[...new2T.values()].filter(Boolean).length}`);
  say(`   distinct task owners OLD / B     : ${oldOwners.size} / ${new2Owners.size}`);

  // A changed-classification is a CHANGE, not automatically a regression: null -> type is a fix,
  // type -> different-type or type -> null is a regression. Report both, judge separately.
  const chg = (nt) => [...oldT.entries()].filter(([id, t]) => nt.get(id) !== t)
    .map(([id, t]) => `${id}: ${t} -> ${nt.get(id)}`);
  const chgA = chg(newT), chgB = chg(new2T);
  say(`   changed vs OLD, variant A        : ${chgA.length}${chgA.length ? '  <-- ' + chgA.join(', ') : ''}`);
  say(`   changed vs OLD, variant B        : ${chgB.length}${chgB.length ? '  <-- ' + chgB.join(', ') : ''}`);

  // AC-3's bar: a change is legitimate ONLY where the spec's own verification map corroborates the
  // new value. Map row -> a review-axis cell means reviewer-checked; an oracle-kind cell means
  // test-backed. Anything else is a real regression.
  for (const [id, oldV] of oldT.entries()) {
    const newV = new2T.get(id); if (newV === oldV) continue;
    const cell = (fs.readFileSync(p, 'utf8').match(new RegExp(`^\\|\\s*${id}\\s*\\|([^|]*)\\|`, 'm')) || [])[1] || '';
    const mapSays = /conformance|security|architecture|axis/i.test(cell) ? 'reviewer-checked'
      : /unit|integration|property|e2e|manual/i.test(cell) ? 'test-backed' : '(map row not found)';
    const ok = mapSays === newV;
    say(`   AC-3 adjudication: ${id} ${oldV} -> ${newV} | map row says "${cell.trim()}" => ${mapSays} | ${ok ? 'CORRECTION (map corroborates)' : 'REGRESSION'}`);
    if (!ok) verdict = false;
  }
  say('');
}
say(verdict ? "VERDICT: variant B introduces no regression. Every changed classification is corroborated by the spec own verification map." : "VERDICT: REGRESSION - a changed classification the verification map does not corroborate.");
